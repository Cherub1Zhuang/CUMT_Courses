function numstable1
%矩阵A中保存前进型迭代法计算结果
%矩阵B中保存后退型迭代法计算结果

%第一个迭代法的初值
I0=0.1823;
%执行第一个迭代法
A=zeros(10,2);A(1,1)=1;A(1,2)=1-5*I0;
for i=1:10
    A(i+1,1)=i+1;
    A(i+1,2)=1/(i+1)-5*A(i,2);
end
A=[0,I0;A];

%执行第二个迭代法
B=zeros(19,2);B(19,1)=19;B(19,2)=0.9167;
for i=19:-1:2 
    B(i-1,1)=i-1;
    B(i-1,2)=(1/i-B(i,2))/5;
end
II0=(1-B(1,2))/5;
B=[0,II0;B];
%显示计算结果
A',B'
